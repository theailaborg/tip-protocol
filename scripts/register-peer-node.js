#!/usr/bin/env node
/**
 * @file tip-protocol/scripts/register-peer-node.js
 * @description Generate a node keypair, register it on a running TIP node, and
 *              emit a ready-to-use .env + keys.json under generated-nodes/<name>/.
 *
 * USAGE:
 *   node scripts/register-peer-node.js --name=node-B --port=4001 --peer=ws://localhost:4000
 *
 * REQUIRES:
 *   1. A running node reachable at --node-url (default http://localhost:4000)
 *   2. genesis-data/founding-vp-keys.json (produced by `npm run seed:fresh`) —
 *      its privateKey is used to sign the council approval.
 */

"use strict";

try { require("dotenv").config(); } catch { /* dotenv optional */ }

const fs = require("fs");
const path = require("path");

const { initCrypto, generateMLDSAKeypair, signBody, shake256 } = require("../shared/crypto");

function parseArg(flag, fallback) {
  const hit = process.argv.find(a => a.startsWith(`${flag}=`));
  return hit ? hit.split("=").slice(1).join("=") : fallback;
}

async function main() {
  await initCrypto();

  const name    = parseArg("--name", `node-${Date.now().toString(36)}`);
  const port    = parseArg("--port", "4001");
  const peer    = parseArg("--peer", "");                             // e.g. ws://localhost:4000
  const nodeUrl = parseArg("--node-url", "http://localhost:4000");    // running node that will register us

  const vpKeysPath = path.resolve(__dirname, "../genesis-data/founding-vp-keys.json");
  if (!fs.existsSync(vpKeysPath)) {
    console.error(`✗ Missing ${vpKeysPath} — run \`npm run seed:fresh\` first.`);
    process.exit(1);
  }
  const vpKeys = JSON.parse(fs.readFileSync(vpKeysPath, "utf8"));
  const vpPrivateKey = vpKeys.privateKey || vpKeys.vp_private_key;
  if (!vpPrivateKey) {
    console.error(`✗ ${vpKeysPath} missing privateKey field.`);
    process.exit(1);
  }

  const seedOutputPath = path.resolve(__dirname, "../genesis-data/seed-output.json");
  if (!fs.existsSync(seedOutputPath)) {
    console.error(`✗ Missing ${seedOutputPath} — run \`npm run seed:fresh\` first.`);
    process.exit(1);
  }
  const seedOutput = JSON.parse(fs.readFileSync(seedOutputPath, "utf8"));
  const approving_vp_id = seedOutput.founding_vp.vp_id;

  const kp = generateMLDSAKeypair();
  const expectedNodeId = shake256(kp.publicKey).slice(0, 16);

  const fields = { name, public_key: kp.publicKey, approving_vp_id };
  const council_signature = signBody(fields, vpPrivateKey);

  const res = await fetch(`${nodeUrl}/v1/node/register`, {
    method:  "POST",
    headers: { "content-type": "application/json" },
    body:    JSON.stringify({ ...fields, council_signature }),
  });
  const body = await res.json().catch(() => ({}));
  if (!res.ok) {
    console.error(`✗ Registration failed (${res.status}):`, body);
    process.exit(1);
  }

  const nodeId = body.node_id || expectedNodeId;
  const outDir = path.resolve(__dirname, "../generated-nodes", name);
  fs.mkdirSync(outDir, { recursive: true });

  const keysJson = {
    created_at:   new Date().toISOString(),
    name,
    node_id:      nodeId,
    public_key:   kp.publicKey,
    private_key:  kp.privateKey,
    algorithm:    kp.algorithm,
    approving_vp: approving_vp_id,
  };
  fs.writeFileSync(path.join(outDir, "keys.json"), JSON.stringify(keysJson, null, 2), { mode: 0o600 });

  const envLines = [
    `# Generated by scripts/register-peer-node.js at ${keysJson.created_at}`,
    `TIP_NODE_ID=${nodeId}`,
    `TIP_NODE_PRIVATE_KEY=${kp.privateKey}`,
    `TIP_NODE_PUBLIC_KEY=${kp.publicKey}`,
    `PORT=${port}`,
    `TIP_DATA_DIR=${path.join(outDir, "data")}`,
    `TIP_DB_PATH=${path.join(outDir, "data", "tip.db")}`,
    peer ? `TIP_PEERS=${peer}` : `# TIP_PEERS=ws://localhost:4000`,
    "",
  ];
  fs.writeFileSync(path.join(outDir, ".env"), envLines.join("\n"), { mode: 0o600 });

  console.log(`✓ Node registered: ${nodeId} (${name})`);
  console.log(`  ${path.join(outDir, "keys.json")}`);
  console.log(`  ${path.join(outDir, ".env")}`);
  console.log(`\nStart this node:`);
  console.log(`  env $(cat ${path.join(outDir, ".env")} | grep -v '^#' | xargs) npm start --prefix node`);
}

main().catch(err => { console.error(err); process.exit(1); });
